//=====================================================================
// 初期化処理
//=====================================================================
void InitGame(void)
{
	// -- Objects --
	InitPlayer();
	InitEnemy();
	InitWeightFollow();
	InitBackground();
	
	// -- Managers --
	InitBulletManager();
	
	// -- Generators --
	InitEnemyGenerator();
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitGame(void)
{
	// -- Objects --
	UninitPlayer();
	UninitEnemy();
	UninitWeightFollow();
	UninitBackground();
	
	// -- Managers --
	UninitBulletManager();
	
	// -- Generators --
	UninitEnemyGenerator();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateGame(void)
{
	// -- Objects --
	UpdatePlayer();
	UpdateEnemy();
	UpdateWeightFollow();
	UpdateBackground();
	
	// -- Managers --
	UpdateBulletManager();
	
	// -- Generators --
	UpdateEnemyGenerator();
}

//=====================================================================
// 描画処理
//=====================================================================
void DrawGame(void)
{
	// -- 2D Objects --
	DrawBackground();
	DrawEnemy();
	DrawBulletManager();
	DrawWeightFollow();
	DrawPlayer();
}