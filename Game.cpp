//=====================================================================
// 初期化処理
//=====================================================================
void InitGame(void)
{
	InitPlayer();
  
	InitEnemy();
  
  InitBulletManager();

  InitBulletFollow();

  InitBackground();

	SetEnemy(D3DXVECTOR3(500, 500, 0));
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitGame(void)
{
	UninitPlayer();
  
  UninitEnemy();

	UninitBulletManager();

	UninitBulletFollow();

	UninitBackground();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateGame(void)
{
	UpdatePlayer();

	UpdateEnemy();
  
  UpdateBulletManager();

  UpdateBulletFollow();

  UpdateBackground();
}

//=====================================================================
// 描画処理
//=====================================================================
void DrawGame(void)
{
	DrawBackground();

	DrawPlayer();
  
	DrawEnemy();
  
  DrawBulletManager();

  DrawBulletFollow();

}