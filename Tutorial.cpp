//=====================================================================
// 初期化処理
//=====================================================================
void InitTutorial(void)
{
	// -- Objects --
	InitBackground();
	InitBackground2();
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitTutorial(void)
{
	// -- Objects --
	UninitBackground();
	UninitBackground2();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateTutorial(void)
{
	// 画面遷移
	if (GetMouseTrigger(MOUSE_LEFT)
		|| GetJoypadTrigger(JOYKEY_A)
		|| GetJoypadTrigger(JOYKEY_START))
	{
		SetFade(SCENE_GAME);
	}

	// -- Objects --
	UpdateBackground();
	UpdateBackground2();
}

//=====================================================================
// 描画処理
//=====================================================================
void DrawTutorial(void)
{
	// -- 2D Objects --
	DrawBackground2();
	DrawBackground();
}